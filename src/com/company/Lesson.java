package com.company;

public class Lesson {

        String subject;
        String theme;

        public Lesson(String subject, String theme) {
            this.subject = subject;
            this.theme = theme;
        }

        public String getSubject() {
            return subject;
        }

        public void setSubject(String subject) {
            this.subject = subject;
        }

        public String getTheme() {
            return theme;
        }

        public void setTheme(String theme) {
            this.theme = theme;
        }


}
